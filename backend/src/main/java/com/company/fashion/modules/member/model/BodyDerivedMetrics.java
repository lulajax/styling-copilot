package com.company.fashion.modules.member.model;

public class BodyDerivedMetrics {

  private Double bmi;
  private Double whr;
  private String shapeClass;

  public Double getBmi() {
    return bmi;
  }

  public void setBmi(Double bmi) {
    this.bmi = bmi;
  }

  public Double getWhr() {
    return whr;
  }

  public void setWhr(Double whr) {
    this.whr = whr;
  }

  public String getShapeClass() {
    return shapeClass;
  }

  public void setShapeClass(String shapeClass) {
    this.shapeClass = shapeClass;
  }
}
